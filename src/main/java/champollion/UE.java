package champollion;

public class UE {
    private final String myIntitule;
    private int myHeuresCM;
    private int myHeuresTD;
    private int myHeuresTP;

    public UE(String intitule) {
        myIntitule = intitule;
    }

    public String getIntitule() {
        return myIntitule;
    }

    public int getHeuresCM() {
        return myHeuresCM;
    }

    public int getHeuresTD() {
        return myHeuresTD;
    }

    public int getHeuresTP() {
        return myHeuresTP;
    }

    
}
